namespace DigitalZenWorks.Common.DatabaseLibrary
{
	/////////////////////////////////////////////////////////////////////////
	/// <summary>
	/// Represents a database column.
	/// </summary>
	/////////////////////////////////////////////////////////////////////////
	public class Column
	{
		private string defaultValue = string.Empty;
		private int length = 255;
		private string name = string.Empty;
		private bool nullable = false;
		private int position = 1;
		private int type = (int)ColumnType.Number;
		private bool unique = false;


		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Represents the default value of the column
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public string DefaultValue
		{
			get { return defaultValue; }
			set { defaultValue = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// The length of the column
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public int Length
		{
			get { return length; }
			set { type = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// The name of the column
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public string Name
		{
			get { return name; }
			set { name = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Represents whether the column can have a null value
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public bool Nullable
		{
			get { return nullable; }
			set { nullable = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Represents the position of the column
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public int Position
		{
			get { return position; }
			set { position = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// The type of the column
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		[System.Diagnostics.CodeAnalysis.SuppressMessage("Microsoft.Naming",
			"CA1721:PropertyNamesShouldNotMatchGetMethods")]
		public int Type
		{
			get { return type; }
			set { type = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Represents whether the column requires unique values
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public bool Unique
		{
			get { return unique; }
			set { unique = value; }
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Default Constructor
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public Column()
		{
		}

		/////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Constructor
		/// </summary>
		/////////////////////////////////////////////////////////////////////
		public Column(string name, int type, int length, bool unique,
			bool nullable, string defaultValue, int position)
		{
			Name = name;
			Type = type;
			Length = length;
			Unique = unique;
			Nullable = nullable;
			DefaultValue = defaultValue;
			Position = position;
		}
	}
}